# Простой safe-format — игнорирует отсутствующие ключи (вместо KeyError возвращает {key})
class _SafeDict(dict):
    def __missing__(self, key):
        return "{" + key + "}"


def render_text(template: str, **vars) -> str:
    """Подставляет переменные в шаблон, не падает если переменной нет."""
    if not template:
        return ""
    try:
        return template.format_map(_SafeDict(vars))
    except Exception:
        # На случай неправильного формата ({0} или подобное)
        return template
